        double valor2;
        double valor3;
        double valor4;
        double valor5;
        double valor6;
        double valor7;
        double valor8;
        double valor9;
        double valor10;

        double resultado1;
        double resultado2;

        valor1=Double.parseDouble(this.txtComprobacion1.getText());
        valor2=Double.parseDouble(this.txtComprobacion2.getText());
        valor3=Double.parseDouble(this.txtComprobacion3.getText());
        valor4=Double.parseDouble(this.txtComprobacion4.getText());
